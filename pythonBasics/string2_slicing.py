b = "Hello, World!"
print(b[2:5])   # gets characters from position 2 to 5(excluding 5)

print(b[:5]) # slicing from start

print(b[2:]) #slicing to the end

print(b[-5:-2]) # gets the character: From "o" in "World!"(position -5) to "d"(position -2) but "d" is excluded

# negative indexing starts from 1 